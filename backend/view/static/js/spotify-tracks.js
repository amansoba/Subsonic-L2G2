function spotifySearchUrl(artistName, trackName) {
  return `${TRACK_SEARCH_BASE}${encodeURIComponent(`${artistName} ${trackName}`)}`;
}

function normalizeSpotifyTracks(tracks, artistName, artistId, fallbackEmbed) {
  return (tracks || [])
    .filter(track => track && track.name)
    .map(track => ({
      id: track.id ? String(track.id) : '',
      name: track.name,
      artistId: artistId || '',
      fallbackEmbed: fallbackEmbed || null,
      searchUrl: track.searchUrl || spotifySearchUrl(artistName, track.name),
    }));
}

export function getSpotifyTracksForArtist(artist) {
  if (!artist) return [];

  const artistName = artist.name || 'Artist';
  const artistId = artist.spotifyArtistId || SPOTIFY_ARTIST_IDS_BY_ARTIST_ID[Number(artist.id)] || '';
  const fallbackEmbed = SPOTIFY_FALLBACK_EMBEDS_BY_ARTIST_ID[Number(artist.id)] || null;

  if (Array.isArray(artist.spotifyTracks) && artist.spotifyTracks.length > 0) {
    return normalizeSpotifyTracks(artist.spotifyTracks, artistName, artistId, fallbackEmbed);
  }

  if (artist.spotifyTrackId) {
    return normalizeSpotifyTracks([{
      id: String(artist.spotifyTrackId),
      name: artist.spotifyTrackName || 'Spotify track',
    }], artistName, artistId, fallbackEmbed);
  }

  const curatedTracks = SPOTIFY_TRACKS_BY_ARTIST_ID[Number(artist.id)];
  return normalizeSpotifyTracks(curatedTracks || [], artistName, artistId, fallbackEmbed);
}

